export default {
  data(){
    let suggestions = [
    "Channel",
    "CodingLab",
    "CodingNepal",
    "YouTube",
    "YouTuber",
    "YouTube Channel",
    "Blogger",
    "Bollywood",
    "Vlogger",
    "Vechiles",
    "Facebook",
    "Freelancer",
    "Facebook Page",
    "Designer",
    "Developer",
    "Web Designer",
    "Web Developer",
    "Login Form in HTML & CSS",
    "How to learn HTML & CSS",
    "How to learn JavaScript",
    "How to became Freelancer",
    "How to became Web Designer",
    "How to start Gaming Channel",
    "How to start YouTube Channel",
    "What does HTML stands for?",
    "What does CSS stands for?",
];
    return{suggestions}
  },

  mounted(){
    this.dynamicSearch();
  },

  methods:{
    dynamicSearch(){
    const searchWrapper = document.querySelector(".search-bar");
    const inputBox = searchWrapper.querySelector("input");
    const suggBox = searchWrapper.querySelector(".autocom-box");
     
      inputBox.onkeyup = (e) => {
        let userData = e.target.value;
        let dynamicArray = [];
        if(e.key === "Backspace") {
          if(userData ===""){
            suggBox.classList.add("invisible");
            }
          }
        else {
          suggBox.classList.remove("invisible"); 
          if(userData){
            dynamicArray = this.suggestions.filter((data) => {
              return data.toLocaleLowerCase().startsWith(userData.toLocaleLowerCase());
            });


            dynamicArray = dynamicArray.map((data) => {
              return data = `<li class="item-list">${data}</li>`;
            })

              
              this.showSuggestions(dynamicArray,inputBox,suggBox);
          }
        }
     
      }
    },
    
    showSuggestions(list,inputBox,suggBox){
    let listData;
    if(!list.length){
      
      try{
      userValue = inputBox.value;
      listData = `<li class="item-list">${userValue}</li>`;
      }catch(e){
          listData = '';
      }
        
    }else{
      listData = list.join('');
    }

    
    console.log(suggBox);
    suggBox.innerHTML = listData;
}//end of method
  }
};
</script>

